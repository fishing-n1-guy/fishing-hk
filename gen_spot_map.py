print("var FISHING_SPOTS = {")
current_region = None
for reg, name, lat, lon in SPOTS:
    nearest = min(STATIONS.keys(), key=lambda s: haversine(lat, lon, STATIONS[s]['lat'], STATIONS[s]['lon']))
    dist = haversine(lat, lon, STATIONS[nearest]['lat'], STATIONS[nearest]['lon'])
    if reg != current_region:
        if current_region: print()
        print(f'  // {reg}')
        current_region = reg
    print(f'  "{name}": {{"stn":"{nearest}", "dist":{dist:.0f}}},')
print("};")
